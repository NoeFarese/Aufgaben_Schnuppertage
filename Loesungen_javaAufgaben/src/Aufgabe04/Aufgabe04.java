package Aufgabe04;

public class Aufgabe04 {
    public static void main(String[] args) {
        for (int i = 0; i <= 10; i++) {
            System.out.println(i);
        }

        System.out.println();
        System.out.println("__________________________________________-");
        System.out.println();

        int x = 0;
        while (x <= 10) {
            System.out.println(x);
            x++;
        }
    }
}
