package Aufgabe04;

public class Aufgabe04 {
    public static void main(String[] args) {
        // Erstelle nun zwei Schleifen, welche die Zahlen von 1 bis 10 ausgeben.
        // Verwende für die erste while und für die zweite for.

        int x = 1;
        while (x <= 10) {
            System.out.println(x);
            x++;
        }

        System.out.println("\n____________________________________________\n");

        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
    }
}
